void display_status(std::vector<char> incorrect, std::string answer) {
  std::cout << "Incorrect Guesses: \n";
    
    for (char c : incorrect) {
      std::cout << c << " ";
    }

    std::cout << "\nCodeword:\n";
    for (int i = 0; i < answer.length(); i++) {
      std::cout << answer[i] << " ";
    }
}

void end_game(std::string codeword, std::string answer) {
  if (answer == codeword) {
    std::cout << "\nHooray! You saved the person and earned a medal of honor!";
  }
  
  else {
    std::cout << "\nOh no! The UFO just flew away with another person!";
  }
}

